import { faker } from '@faker-js/faker';
import { matchSorter } from 'match-sorter'; // For filtering
import { Product } from './data';
export const delay = (ms: number) =>
  new Promise((resolve) => setTimeout(resolve, ms));

// Mock product data store
export const fakeProducts = {
  records: [] as Product[], // Holds the list of product objects

  // Initialize with sample data
  initialize() {
    const sampleProducts: Product[] = [];
    function generateRandomProductData(id: number): Product {
      const states = [
        'selling',
        'creating schedule',
        'reviewing by applicant',
        'reviewing by property owner',
        'reviewing by Jads lawyer',
        'reviewing by Jads admin',
        'esigning',
        'sold'];
      return {
        id,
        // name: faker.commerce.productName(),
        name: faker.helpers.rangeToNumber({ min: 1, max: 100 }).toString() + faker.helpers.arrayElement(['A', 'B', 'C']),
        description: faker.helpers.arrayElement([faker.company.name(), 'NULL']),
        price: parseFloat(faker.commerce.price({ min: 500, max: 3000, dec: 2 })),
        size: 1,
        release_time: faker.date
          .between({ from: '2022-01-01', to: '2023-12-31' })
          .toISOString(),
        image: `https://api.slingacademy.com/public/sample-products/${id}.png`,
        category: faker.helpers.arrayElement(['Jadsgroup', 'xxx']),
        state: faker.helpers.arrayElement(states),
        owner: "1",
        agent: "1"
      };
    }

    // Generate remaining records
    for (let i = 1; i <= 20; i++) {
      sampleProducts.push(generateRandomProductData(i));
    }

    this.records = sampleProducts;
  },

  // Get all products with optional category filtering and search
  async getAll({
    categories = [],
    search
  }: {
    categories?: string[];
    search?: string;
  }) {
    let products = [...this.records];

    // Filter products based on selected categories
    if (categories.length > 0) {
      products = products.filter((product) =>
        categories.includes(product.category)
      );
    }

    // Search functionality across multiple fields
    if (search) {
      products = matchSorter(products, search, {
        keys: ['name', 'description', 'category']
      });
    }

    return products;
  },

  // Get paginated results with optional category filtering and search
  async getProducts({
    page = 1,
    limit = 10,
    categories,
    search
  }: {
    page?: number;
    limit?: number;
    categories?: string;
    search?: string;
  }) {
    await delay(1000);
    const categoriesArray = categories ? categories.split('.') : [];
    const allProducts = await this.getAll({
      categories: categoriesArray,
      search
    });
    const totalProducts = allProducts.length;

    // Pagination logic
    const offset = (page - 1) * limit;
    const paginatedProducts = allProducts.slice(offset, offset + limit);

    // Mock current time
    const currentTime = new Date().toISOString();

    // Return paginated response
    return {
      success: true,
      time: currentTime,
      message: 'Sample data for testing and learning purposes',
      total_products: totalProducts,
      offset,
      limit,
      products: paginatedProducts
    };
  },

  // Get a specific product by its ID
  async getProductById(id: number) {
    await delay(1000); // Simulate a delay

    // Find the product by its ID
    const product = this.records.find((product) => product.id === id);

    if (!product) {
      return {
        success: false,
        message: `Product with ID ${id} not found`
      };
    }

    // Mock current time
    const currentTime = new Date().toISOString();

    return {
      success: true,
      time: currentTime,
      message: `Product with ID ${id} found`,
      product
    };
  }
};

// Initialize sample products
fakeProducts.initialize();
